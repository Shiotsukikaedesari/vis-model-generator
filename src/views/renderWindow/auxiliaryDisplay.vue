      @click.stop="() => {showTransformControls = !showTransformControls}">
      <vis-icon size="16px" code="#iconkongjian1"></vis-icon>
    </div> -->
  </div>
</template>

<script>
export default {
  props: {
    sceneConnector: {
      type: Object,
      required: true,
    },
    engineConnector: {
      type: Object,
      required: true,
    },
  },
  data() {
    return {
      showAxes: true,
      showGrid: true,
      showHelper: true,
      showTransformControls: true,
    };
  },
  watch: {
    showAxes: {
      handler(newVal) {
        this.sceneConnector.showAxesHelper(newVal);
      },
      immediate: true,
    },
    showGrid: {
      handler(newVal) {
        this.sceneConnector.showGridHelper(newVal);
      },
      immediate: true,
    },
    showHelper: {
      handler(newVal) {
        this.sceneConnector.showObjectHelper(newVal);
      },
      immediate: true,
    },
    showTransformControls: {
      handler(newVal) {
        this.engineConnector.setTransformControls(newVal);
      },
      immediate: true,
    },
  },
};
</script>

